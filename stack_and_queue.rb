#here is a example of stack and queue

#stack
stack = []

#push to stack
stack.push(1)
stack.push(2)
stack.push(3)

#pop from stack
puts stack.pop()
puts stack.pop()
puts stack.pop()

#queue
queue = []

#push to queue
queue.push(1)
queue.push(2)
queue.push(3)

#pop from queue

puts queue.shift()
puts queue.shift()
puts queue.shift()